    int intentos = 0;

    public void login() {

        intentos++;

        if (intentos > 3) {
            
            JOptionPane.showMessageDialog(null, "Demasiados Intentos");
            this.btn_In.setEnabled(false);

        } else {

            String user = "pabloski";
            String password = "12345";

            String txtUser, txtPassword;

            txtUser = this.txt_User.getText();
            txtPassword = this.txt_Password.getText();

            if (txt_User.equals("") || txt_Password.equals("")) {

                JOptionPane.showMessageDialog(null, "Ingresa todos los datos.");

            } else {

                if (txtUser.equals(user) && txtPassword.equals(password)) {

                    JOptionPane.showMessageDialog(null, "Bienvenido " + txtUser + ".");

                    Principal p = new Principal();
                    p.setVisible(true);
                    this.setVisible(false);

                } else {

                    JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrecto.");

                }
            }
        }
    }

